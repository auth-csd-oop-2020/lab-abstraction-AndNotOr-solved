/**
 * Η παρακάτω abstract κλάση αναπαριστά έναν δυαδικό λογικό τελεστή. Οι μεταβλητές left και right αναπαριστούν τους
 * τελεσταίους αριστερά και δεξιά του τελεστή αντίστοιχα.
 *
 * The class below represents a binary logical operators. The variables left and right represent the operands on the left
 * and on the right of the operator accordingly.
 */
public abstract class BinaryOperator implements Expression{
    protected Expression left;
    protected Expression right;

    public BinaryOperator(Expression left, Expression right) {
        this.left = left;
        this.right = right;
    }
}

